#include "gtest/gtest.h"
#include "Point.hpp"

TEST(Point, getX)
{
    ced::cpu::Point p(4, 5);
    EXPECT_EQ(4, p.x);
}
//--------------------------------------------------------------------------------
TEST(Point, getY)
{
    ced::cpu::Point p(4, 5);
    EXPECT_EQ(5, p.y);
}
//--------------------------------------------------------------------------------
TEST(Point, setX)
{
    ced::cpu::Point p;
    p.x = 3;
    EXPECT_EQ(3, p.x);
}
//--------------------------------------------------------------------------------
TEST(Point, setY)
{
    ced::cpu::Point p;
    p.y = 4;
    EXPECT_EQ(4, p.y);
}
//--------------------------------------------------------------------------------


